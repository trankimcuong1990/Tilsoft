        public override DTO.SearchFormData GetDataWithFilter(System.Collections.Hashtable filters, int pageSize, int pageIndex, string orderBy, string orderDirection, out int totalRows, out Library.DTO.Notification notification)
        {
            //throw new NotImplementedException();

            notification = new Library.DTO.Notification() { Type = Library.DTO.NotificationType.Success };
            DTO.SearchFormData data = new DTO.SearchFormData();
            data.Data = new List<DTO.DeliveryNoteSearch>();
            totalRows = 0;

            string DeliveryNoteUD = null;
            string ReceiverName = null;
            string ReceiverAddress = null;
            string Description = null;

            if (filters.ContainsKey("DeliveryNoteUD") && !string.IsNullOrEmpty(filters["DeliveryNoteUD"].ToString()))
            {
                DeliveryNoteUD = filters["DeliveryNoteUD"].ToString().Replace("'", "''");
            }
            if (filters.ContainsKey("ReceiverName") && !string.IsNullOrEmpty(filters["ReceiverName"].ToString()))
            {
                ReceiverName = filters["ReceiverName"].ToString().Replace("'", "''");
            }
            if (filters.ContainsKey("ReceiverAddress") && !string.IsNullOrEmpty(filters["ReceiverAddress"].ToString()))
            {
                ReceiverAddress = filters["ReceiverAddress"].ToString().Replace("'", "''");
            }
            if (filters.ContainsKey("Description") && !string.IsNullOrEmpty(filters["Description"].ToString()))
            {
                Description = filters["Description"].ToString().Replace("'", "''");
            }
            
           
            //try to get data
            try
            {
                using (var context = CreateContext())
                {

                    totalRows = context.VanPhatMng_Function_DeliveryNote_Search(orderBy, orderDirection, DeliveryNoteUD, ReceiverName, ReceiverAddress, Description).Count();
                    var result = context.VanPhatMng_Function_DeliveryNote_Search(orderBy, orderDirection, DeliveryNoteUD, ReceiverName, ReceiverAddress, Description);
                    data.Data = converter.DB2DTO_DeliveryNoteView(result.Skip(pageSize * (pageIndex - 1)).Take(pageSize).ToList());
                }
            }
            catch (Exception ex)
            {
                notification.Type = Library.DTO.NotificationType.Error;
                notification.Message = ex.Message;
            }

            return data;
        }

