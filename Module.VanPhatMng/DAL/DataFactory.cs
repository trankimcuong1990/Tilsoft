        public object GetDetailProductionItemList(System.Collections.Hashtable filters, out Library.DTO.Notification notification)
        {
            List<DTO.DeliveryNoteDetailSearch> data = new List<DTO.DeliveryNoteDetailSearch>();
            notification = new Library.DTO.Notification { Type = Library.DTO.NotificationType.Success };

            var deliverynoteID = (filters.ContainsKey("deliveryNoteID") && filters["deliveryNoteID"] != null && !string.IsNullOrEmpty(filters["deliveryNoteID"].ToString())) ? Convert.ToInt32(filters["deliveryNoteID"].ToString()) : 0;
            using (var context = CreateContext())
            {
                var dbItem = context.VanPhatMng_DeliveryNoteDetail_SearchView.Where(o => o.DeliveryNoteID == deliverynoteID).ToList();
                data = converter.DB2DTO_GetListDetail(dbItem);
            }
            return data;
        }

        public DTO.VPPrintOutDTO GetDeliveryNotePrintoutHTML( System.Collections.Hashtable filters, out Library.DTO.Notification notification)
        {
            notification = new Library.DTO.Notification() { Type = Library.DTO.NotificationType.Success };
            DTO.VPPrintOutDTO printOut = new DTO.VPPrintOutDTO();
            printOut.deliveryNoteDetailPrintOutDTOs = new List<DTO.VPDeliveryNoteDetailPrintOutDTO>();
            ReportDataObject ds = new ReportDataObject();
            

            string deliveryNoteIDs =(filters.ContainsKey("deliveryNoteID") && filters["deliveryNoteID"] != null && !string.IsNullOrEmpty(filters["deliveryNoteID"].ToString())) ? Convert.ToString(filters["deliveryNoteID"].ToString().Replace("'", "''")): " ";
            var itemdeliveryNoteIDs = deliveryNoteIDs.Split(',');

            try
            {
                if (string.IsNullOrEmpty(deliveryNoteIDs))
                {
                    throw new Exception("You need select Item before export");
                }

                SqlDataAdapter adap = new SqlDataAdapter();
                adap.SelectCommand = new SqlCommand("VanPhatMng_function_GetExport", new SqlConnection(Library.Helper.GetSQLConnectionString()));
                adap.SelectCommand.CommandType = System.Data.CommandType.StoredProcedure;
                adap.SelectCommand.Parameters.AddWithValue("@DeliveryNoteIDs", deliveryNoteIDs);
                adap.TableMappings.Add("Table", "DetailList");
                adap.Fill(ds);


                //Read DeliveryNoteDetail 

                DTO.VPDeliveryNoteDetailPrintOutDTO deliveryNoteDetailPrintOut;

                foreach(var item in ds.DetailList)
                {
                    deliveryNoteDetailPrintOut = new DTO.VPDeliveryNoteDetailPrintOutDTO();
                    printOut.deliveryNoteDetailPrintOutDTOs.Add(deliveryNoteDetailPrintOut);

                    deliveryNoteDetailPrintOut.DeliveryNoteID = item.DeliveryNoteID;
                    deliveryNoteDetailPrintOut.ProductionItemNM = item.ProductionItemNM;
                    deliveryNoteDetailPrintOut.ProductionItemUD = item.ProductionItemUD;
                    deliveryNoteDetailPrintOut.ComponentMaterialNM = item.ComponentMaterialNM;
                    deliveryNoteDetailPrintOut.MaterialMaterialNM = item.MaterialMaterialNM;
                    deliveryNoteDetailPrintOut.ComponentFrameMaterial = item.ComponentFrameMaterial;
                    deliveryNoteDetailPrintOut.MaterialFrameMaterial = item.MaterialFrameMaterial;
                    deliveryNoteDetailPrintOut.UnitNM = item.UnitNM;
                    deliveryNoteDetailPrintOut.ComponentQty = item.ComponentQty;
                    deliveryNoteDetailPrintOut.MaterialQty = item.MaterialQty;
                    deliveryNoteDetailPrintOut.Description = item.Description;
                    deliveryNoteDetailPrintOut.PrimaryProduct = item.PrimaryProduct;
                }

                using (var context = CreateContext())
                {
                    foreach (var itemID in itemdeliveryNoteIDs)
                    {
                        if (itemID != "")
                        {
                            var deliveryNoteID = Convert.ToInt32(itemID);
                            var dbItem = context.DeliveryNote.FirstOrDefault(o => o.DeliveryNoteID == deliveryNoteID);

                            if(dbItem.DeliveryNoteID == deliveryNoteID)
                            {
                                dbItem.IsPrinted = true;
                                context.SaveChanges();
                            }


                        }
                    }
                };


                return printOut;
            }
            catch (Exception ex)
            {
                notification.Type = Library.DTO.NotificationType.Error;
                notification.Message = ex.Message;
                notification.DetailMessage.Add(ex.Message);
                if (ex.GetBaseException() != null)
                {
                    notification.DetailMessage.Add(ex.GetBaseException().Message);
                }
                return printOut;
            }
        }
