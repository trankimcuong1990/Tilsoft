        public object CustomFunction(int userId, string identifier, System.Collections.Hashtable filters, out Library.DTO.Notification notification)
        {
            notification = new Library.DTO.Notification() { Type = Library.DTO.NotificationType.Success };
            switch (identifier.Trim())
            {
                case "EstimateMaterial":
                    string factoryOrderIds = filters["factoryOrderIds"].ToString();
                    return bll.EstimateMaterial(userId, factoryOrderIds, out notification);
                case "ImportMaterial":
                    object dtoItem = filters["dtoItem"];
                    return bll.ImportMaterial(userId, dtoItem, out notification);
                case "GetSupportData":
                    return bll.GetSupportData(userId, out notification);
                default:
                    notification.Message = "function identifier do not match";
                    notification.Type = Library.DTO.NotificationType.Error;
                    break;
            }
            return null;
        }
